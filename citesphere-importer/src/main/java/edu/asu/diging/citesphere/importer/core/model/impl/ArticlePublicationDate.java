package edu.asu.diging.citesphere.importer.core.model.impl;

public class ArticlePublicationDate {

    private String publicationDay;
    private String publicationMonth;
    private String publicationYear;
    private String publicationDate;
    private String publicationDateType;
    
    public String getPublicationDay() {
        return publicationDay;
    }
    public void setPublicationDay(String publicationDay) {
        this.publicationDay = publicationDay;
    }
    public String getPublicationMonth() {
        return publicationMonth;
    }
    public void setPublicationMonth(String publicationMonth) {
        this.publicationMonth = publicationMonth;
    }
    public String getPublicationYear() {
        return publicationYear;
    }
    public void setPublicationYear(String publicationYear) {
        this.publicationYear = publicationYear;
    }
    public String getPublicationDate() {
        return publicationDate;
    }
    public void setPublicationDate(String publicationDate) {
        this.publicationDate = publicationDate;
    }
    public String getPublicationDateType() {
        return publicationDateType;
    }
    public void setPublicationDateType(String publicationDateType) {
        this.publicationDateType = publicationDateType;
    }
    
    
}
